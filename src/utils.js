export const convertToRoman = num => {
  switch (num) {
    case 1:
      return "I";
      /* eslint-disable no-unreachable */
      break;
    case 2:
      return "II";
      /* eslint-disable no-unreachable */
      break;
    case 3:
      return "III";
      /* eslint-disable no-unreachable */
      break;
    case 4:
      return "IV";
      /* eslint-disable no-unreachable */
      break;
    case 5:
      return "V";
      /* eslint-disable no-unreachable */
      break;
    case 6:
      return "VI";
      /* eslint-disable no-unreachable */
      break;
    default:
      return "";
      /* eslint-disable no-unreachable */
      break;
  }
};
